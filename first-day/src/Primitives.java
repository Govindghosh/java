public class Primitives{
    public static void main(String[] args) {
        int number = 100;
        long largerNumber = 7906630435L;
        float decimal = 88.56F;
        double largerDecimal = 65456.54564;
        char letter = 'h';
        boolean check = true;
        System.out.println("this is int : " + number);
        System.out.println("this is long : " + largerNumber);
        System.out.println("this is float : " + decimal);
        System.out.println("this is double : " + largerDecimal);
        System.out.println("this is char : " + letter);
        System.out.println("this is check boolean: " + check);
    }
}
